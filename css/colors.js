`COLORS`;
// There are several ways to specify colors in CSS, including color names, hexadecimal values, RGB values, and HSL values.
// There are also two main colour properties in CSS: color and background-color.

`COLOR PROPERTY`;
// The color property is used to set the text color of an element. It accepts a variety of color values, including color names, hexadecimal values, RGB values, and HSL values.

// Example:
`p {
    color: red;
}`;

// In this example, the color property sets the text color of all <p> elements to red.


`BACKGROUND-COLOR PROPERTY`;
// The background-color property is used to set the background color of an element. It accepts the same variety of color values as the color property.

// Example:
`body {
    background-color: lightblue;
}`;

// In this example, the background-color property sets the background color of the <body> element to light blue.







`COLOR MODELS`;
// There are several color models that can be used to specify colors in CSS, including RGB (Red, Green, Blue) and HSL (Hue, Saturation, Lightness).

`HSL MODEL`;
// The HSL color model is a way of specifying colors using hue, saturation, and lightness values. The hue value represents the color itself, the saturation value represents the intensity of the color, and the lightness value represents the brightness of the color.

// Example:
`h1 {
    color: hsl(240, 100%, 50%);
}`;

// In this example, the color property sets the text color of all <h1> elements to a shade of blue using the HSL color model.


`RGB MODEL`;
// The RGB color model is a way of specifying colors using red, green, and blue values. Each color component is represented by an integer value between 0 and 255.

// Example:
`h1 {
    color: rgb(0, 0, 255);
}`;

// In this example, the color property sets the text color of all <h1> elements to blue using the RGB color model.

// This is the model hexadecimal values are based on.









`COLOR SYSTEMS`;
// CSS provides several color systems for specifying colors. Some common color systems include RGB, HSL, and hexadecimal.

`COLOR NAMES`;
// CSS provides a set of predefined color names that can be used to specify colors. Some common color names include red, blue, green, yellow, and black. These are syntactic sugar for the RGB values of the colors.

// Example:
`h1 {
    color: blue;
}`;

// In this example, the color property sets the text color of all <h1> elements to blue.

`HEXADECIMAL VALUES`;
// Hexadecimal values are a way to specify colors using a six-digit code that represents the RGB values of the color. Each pair of digits represents the red, green, and blue components of the color, respectively.

// Example:
`h1 {
    color: #0000FF;
}`;

// In this example, the color property sets the text color of all <h1> elements to blue using a hexadecimal value.

// The first two values represent the red component, the second two values represent the green component, and the last two values represent the blue component. Each of them can be a value between 00 and FF, which corresponds to 0 and 255 in decimal.

