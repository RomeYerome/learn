`BORDERS`;
// Borders create a visual boundary around an element. They can be styled using various properties such as border-width, border-style, and border-color.

`Border-Width`;
// The border-width property is used to specify the width of the border. It accepts values such as thin, medium, thick, and length values like 1px, 2px, etc.


`Border-Style`;
// The border-style property is used to specify the style of the border. It accepts values such as none, hidden, dotted, dashed, solid, double, groove, ridge, inset, outset, and more.


`Border-Color`;
// The border-color property is used to specify the color of the border. It accepts color values like red, #ff0000, rgb(255,0,0), etc.


`Border`;
// The border property is a shorthand property that combines the border-width, border-style, and border-color properties into a single declaration. It accepts values like 1px solid red, 2px dashed #00ff00, etc.


`Individual Borders`;
// You can also use individual properties like border-top, border-right, border-bottom, and border-left to style specific sides of the border.






`Border-Radius`;
// The border-radius property is used to create rounded corners on an element's border. It accepts length values like 5px, 10px, etc. to specify the radius of the corners.

// You can also specify different radii for each corner using the border-top-left-radius, border-top-right-radius, border-bottom-left-radius, and border-bottom-right-radius properties.


`Border-Image`;
// The border-image property is used to specify an image to be used as the border of an element. It accepts values like url('border.png') 10 10 10 10 stretch, where the first value is the image source, the next four values are the border slice values, and the last value is the border repeat value.


`Outline`;
// The outline property is used to create a line around an element outside the border. It is similar to the border property but does not affect the layout of the document. It accepts values like 1px solid red, 2px dashed #00ff00, etc.


`Outline-Offset`;
// The outline-offset property is used to specify the space between the outline and the border of an element. It accepts length values like 5px, 10px, etc. to specify the offset distance.


