`VARIABLES`;
// variables in SASS are used to store information that you want to reuse throughout your stylesheet. You can store things like colors, font sizes, spacing values, and more in variables. Variables in SASS are defined using the $ symbol followed by the variable name and the value you want to assign to the variable. Here is an example of how to define a variable in SASS:

`$primary-color: #333;`;
`$font-stack: Arial, sans-serif;`;

// In this example, we defined a variable called $primary-color and assigned it the value #333. You can then use this variable throughout your stylesheet by referencing the variable name. Here is an example of how to use a variable in SASS:

`body {
    color: $primary-color;
}`;

// Sass makes working with variables very easy