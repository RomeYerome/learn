`HOSTING ON NETLIFY`;

// Platforms like netlify and vercel take your code from a github repository and deploy it to the web. You'll have to specify a build command that produces your production files. They also provide a lot of features like serverless functions, form handling, and more. Netlify is a great platform for hosting static sites and serverless functions. Here are the steps to deploy a static site on netlify:

// First you'll need to create a github repository and push your project to it. Then follow these steps:

// 1. Sign up or sign into netlify with github
// 2. Click on the new site from git button on the dashboard
// 3. Search for the project you want to upload
// 4. Next enter the build settings (build command e.g npm run build, publish directory: dist/)
// 5. Click on deploy site