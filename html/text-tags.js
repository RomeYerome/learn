`HEADING TAGS`;
// Heading tags are used to define headings on a webpage. There are 6 heading tags in HTML, from <h1> to <h6> and they are ranked in order of importance with h1 being the most important heading, and the <h6> tag, the least important heading.

// Heading tags define the structure of a webpage and give context to the content on the page. They create hierarchy of content on a webpage and are important in SEO. The heading tags are used to define the main headings and subheadings on a webpage.

// The default appearance of heading tags is bold and larger than normal text. The <h1> tag is the largest and boldest heading and the <h6> tag is the smallest and least bold heading. The appearance of heading tags can be changed using CSS.

// The heading tag is a block level element. i.e it takes up the full width of the page and starts on a new line.
`<h1>Heading 1</h1>`;
`<h2>Heading 2</h2>`;
`<h3>Heading 3</h3>`;
`<h4>Heading 4</h4>`;
`<h5>Heading 5</h5>`;
`<h6>Heading 6</h6>`;


`PARAGRAPH TAG <p>`;
// The paragraph tag is used to define a paragraph on a webpage. It is a container tag that contains text content. The content inside the <p> tag is displayed as a paragraph on the webpage.

// The paragraph tag is a block level element. i.e it takes up the full width of the page and starts on a new line.

`<p>This is a paragraph</p>`;

// In VS Code you can get dummy text by writing lorem inside a paragraph tag or lorem 10 if you want a 10 word paragraph




`Strong Tag <strong>`;
// The strong tag is used inside a paragraph. Semantically it means strongly emphasize. It shows up as a bold text on the webpage. It is used to highlight important text on a webpage.

`<p>This is a <strong>strong</strong> text</p>`;




`Em Tag <em>`;
// The em tag is used inside a paragraph. Semantically it means emphasize. It shows up as italic text on the webpage. It is used to emphasize text on a webpage.

`<p>This is an <em>emphasized</em> text</p>`;





`Break Tag <br>`;
// The break tag is a self closing tag. It is used to create a line break in a paragraph. 

`<p>This is a line of text<br>This is another line of text</p>`;






`Horizontal Rule Tag <hr>`;
// The horizontal rule tag is a self closing tag. It is used to create a horizontal line within a body of text.

`<p>This is a line of text<hr>This is another line of text</p>`;

`<p>This is a paragraph</p>`;




`Span Tag <span>`;
// The span tag is a container tag. It is used to group inline elements on a webpage. It is used to apply styles to a part of a text on a webpage.

`<p>This is a <span style="color: red;">red</span> text</p>`;

// The span tag is an inline element. i.e it takes up only the width of the content inside it and does not start on a new line.




`Deleted Tag <del>`;
// The deleted tag is used to represent deleted text on a webpage. It is used to show text that has been removed from a document. It appears as a strikethrough text on the webpage.

`<p>This is a <del>deleted</del> text</p>`;





`KEY ATTRIBUTES FOR TEXT TAGS`;
// Attributes are used to provide additional information about an element. They are always included in the opening tag and are written as name-value pairs separated by an equals sign. 

// The attributes for text tags are:

`style`; // The style attribute is used to add inline styles to an element. It is used to change the appearance of an element. The style attribute takes CSS properties and values as its value. e.g <p style="color: red;">This is a red text</p>

`class`; // The class attribute is used to assign CSS styles to elements. e.g <p class="paragraph">This is a paragraph</p>

`id`; // The id attribute is used to uniquely identify an element on a webpage. e.g <p id="paragraph">This is a paragraph</p>

`title`; // The title attribute is used to provide additional information about an element. It is displayed as a tooltip when the mouse hovers over the element. e.g <p title="This is a paragraph">This is a paragraph</p>

