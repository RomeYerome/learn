`META TAGS`;
// Meta tags are tags that provide information about the HTML document. They are not displayed on the webpage but are used by search engines, browsers, and other web services. They are placed inside the head tag of an HTML document.

// Meta tags carry different information and they are Some of the most common ones are:

`<meta charset="UTF-8">`;   // Specifies the character encoding for the HTML document. UTF-8 is the default. It supports all characters and symbols in the Unicode standard. You don't need to specify this tag if you are using UTF-8 as it's the default.

`<meta name="viewport" content="width=device-width, initial-scale=1.0">`; // This tag is essential for responsive design. It tells the browser to set the width of the page to the width of the device and set the initial zoom level to 1.0. 

`<meta http-equiv="X-UA-Compatible" content="ie=edge">`; // This tag is used to set the compatibility mode for Internet Explorer. It tells the browser to use the latest rendering engine available.

`<meta name="description" content="A description of the page">`; // This tag provides a description of the page. It is used by search engines to display a snippet of the page in search results. It should be a short and concise description of the page.

`<meta name="keywords" content="keyword1, keyword2, keyword3">`; // This tag provides keywords for the page. It is used by search engines to index the page. It should contain a list of relevant keywords separated by commas.

`<meta name="author" content="Author Name">`; // This tag provides the name of the author of the page. It is used by search engines to display the author of the page in search results.

`<meta name="robots" content="index, follow">`; // This tag tells search engines whether to index the page and follow the links on the page. The values can be index, noindex, follow, nofollow.

`<meta name="revised" content="Sunday, July 18th, 2021, 5:15 pm">`; // This tag provides the date and time the page was last updated. It is used by search engines to determine how often to crawl the page.


