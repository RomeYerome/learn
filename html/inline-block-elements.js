// Every HTML Element has a default display value that affects how they appear on a page. The most common display values are block, inline, and inline-block. 




`BLOCK ELEMENTS`;
// Block-level elements start on a new line and take up the full width available. You can change the width and height of a block-level element. A block-level element will not let any other element sit next to it on the page even if you reduce the width, It'll still force the next element to a new line.

`address, article, aside, blockquote, canvas, dd, div, dl, dt, fieldset, figcaption, figure, footer, form, h1, -h6, header, hr, li, main, nav, noscript, ol, p, pre, section, table, tfoot, ul, video`;

// Block level elements have a default margin top and bottom


`INLINE ELEMENTS`;
// Inline elements do not start on a new line and only take up as much width as its content. Inline elements can sit next to each other and do not force the next element to a new line. However, you can not change the width and height of an inline element. 

// You also can not add margin or padding on the top and bottom of an inline element (You can but it will be ignored) because it will alter the flow of content.

`a, abbr, acronym, b, bdo, big, br, button, cite, code, dfn, em, i, img, input, kbd, label, map, object, output, q, samp, script, select, small, span, strong, sub, sup, textarea, time, tt, var`;

// An inline element can not contain/wrap a block element


`INLINE-BLOCK ELEMENTS`;
// Inline-block elements are similar to inline elements, but they can have a width and height. This means that they can have margin and padding added to the top and bottom of the element. Inline-block elements will not force a new line to start and can sit next to each other. They are a middle ground between block and inline elements.


`NOTE`;
`You can change the display value of an element by using the display property in CSS.`;
