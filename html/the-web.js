`THE WORLD WIDE WEB`;
// Is a global network of computers that are connected to each other using the Internet Protocol (IP) and Transmission Control Protocol (TCP). The Web is a system of interlinked hypertext documents that are accessed via the Internet. The Web is often confused with the Internet, but the two are not the same thing. The Internet is the infrastructure that connects computers together, while the Web is the system of documents that are accessed via the Internet. 

`IP and TCP`;
// Every computer/router has an IP address. This is a unique identifier that is used to locate a computer on a network. The IP address is a series of numbers separated by dots e.g 192.168.x.x. The IP address is used to route data between computers on a network.

// Your public IP address which is the one used to connect to the internet is assigned by your Internet Service Provider (ISP). 

// The Transmission Control Protocol (TCP) is a protocol that is used to send data between computers on a network. TCP is responsible for breaking data into packets, sending the packets, and reassembling the packets at the other end. TCP ensures that data is sent reliably and in the correct order.


`HTTP and HTTPS`;
// stands for Hypertext Transfer Protocol. It is the protocol that is used to transfer data between a web server and a web browser. It handles web traffic (response/request) and is the foundation of any data exchange on the Web.

// HTTPS stands for Hypertext Transfer Protocol Secure. It is the secure version of HTTP. It uses encryption to secure the data that is transferred between a web server and a web browser. HTTPS is used to protect sensitive data such as passwords, credit card numbers, and other personal information.


`WEBSITES and WEBPAGES`;
// A website is a collection of webpages that are linked together. Webpages are basically files/documents stored on a web server (e.g Apache). When you visit a website, your web browser requests the webpage from the web server and the server sends the webpage back to your browser.


`HOSTING`;
// Hosting companies provide space on a server for your websites to be stored. When you purchase hosting, you are basically renting space on a server to store your website files. Hosting companies also provide other services such as email hosting, domain registration, and website building tools.

// You can get a dedicated server, which is basically you having a computer to yourself. Or you can get shared hosting where you share a server with other websites. Shared hosting is cheaper but can be slower because you are sharing resources with other websites.


`DOMAIN NAMES`;
// Domain names are aliases for IP addresses. Remember that each computer/router has an ip address. If you type the ip address for a website into your browser, you will be able to access the website. But it is easier to remember a domain name than an IP address. Domain names are used to identify websites on the Internet.


`DNS`;
// stands for Domain Name System. It is a system that translates domain names into IP addresses. When you type a domain name into your browser, the browser sends a request to a DNS server to translate the domain name into an IP address. The DNS server then sends the IP address back to the browser so that the browser can connect to the website.


