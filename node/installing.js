`WHAT IS NODE JS?`;
// Node JS is a Javascript runtime environment. It allows JS to run on the server, much like other languages e.g Python, Ruby, Java etc.

// Node uses the V8 engine which is the same engine used by Chrome to run JS in the browser. The inner workings are similar to the browser. It has a call stack, event loop, callback queue, etc. but obviously there is no DOM, no Window or Document in Node. It's own global object is called GLOBAL


`INSTALLING NODE JS`;
// To install Node JS, go to the official Node JS website (nodejs.org) and download the installer for your operating system. Node JS comes with npm (Node Package Manager) which is used to install 3rd party packages. You can check if Node JS is installed by running the following commands in your terminal:

`node -v`;

`npm -v`;

// If you see the version numbers of Node JS and npm, then Node JS is installed on your system. You can now start writing Node JS code in your text editor and run it in the terminal using the node command. To run a Node JS file, save it with a .js extension and run the following command in your terminal:

`node filename.js`;

// This will run the Node JS file and output the result in the terminal. You can also use the console.log() function to output data to the terminal. Node JS is a powerful tool for building server-side applications and APIs. It is widely used in the industry and has a large ecosystem of packages that you can use to add functionality to your applications.